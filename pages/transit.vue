</li>
<li>
<p>Carpooling significantly reduces vehicular emissions and HOV lanes encourage carpooling when population density is high.<a href="#fn19" class="footnote-ref" id="fnref19" role="doc-noteref"><sup>19</sup></a></p>
</li>
</ol></li>
<li>
<p>Proposals</p>

