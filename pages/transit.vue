<li><h3 id="cycling">Cycling</h3>
<ol start="3" type="1">
<li>
<p>Data</p>

<ol start="15" type="1">
<li>
<p>Cycling in the Netherlands prevents 6500 deaths per year, increases life expectancy by half a year, and accounts for more than 3% of the GDP through health benefits.<a href="#fn13" class="footnote-ref" id="fnref13" role="doc-noteref"><sup>13</sup></a></p>
</li>
<li>
<p>Cycling lanes reduce road fatalities for all users due to the “calming effect” that bicycle lanes have on automobile traffic.<a href="#fn14" class="footnote-ref" id="fnref14" role="doc-noteref"><sup>14</sup></a></p>
</li>
<li>
<p>Opposition to bike lanes is partly conducted by small business owners who often underestimate the effect of bike traffic on business and worry about temporary loss of business during construction. However, these fears tend to be largely unfounded, so outreach with business owners with data from similar projects will help convince them. Conservative bikelash tends to be founded on xenophobia and racism, so cultivating a friendly image of a cyclist helps combat fear campaigns.<a href="#fn15" class="footnote-ref" id="fnref15" role="doc-noteref"><sup>15</sup></a></p>
</li>
</ol></li>
<li>
<p>Proposals</p>

<ol start="18" type="1">
<li><h3 id="increase-distribution-and-connectedness-of-bike-lanes-such-as-between-tuscany-and-the-university.">Increase distribution and connectedness of bike lanes, such as between Tuscany and the University.</h3></li>
<li>
<p>Dedicate a street downtown specifically for cyclists and pedestrians.</p>
</li>
<li>
<p>Promote cycling by emphasizing health and economic benefits with a wholesome angle.</p>
</li>
</ol></li>
</ol></li>
<li>
<p>Pedestrians</p>

<ol start="5" type="1">
<li>
<p>Data</p>

<ol start="21" type="1">
