<h1 id="transit">Transit</h1>
<h2 id="introduction">Introduction</h2>
<p>Expanding public transit is an integral component of climate action. Not only does public transit reduce carbon emissions, it also reduces road maintenance and increases economic activity. The following data and proposals in the Transit section of the Maximum GND seek to optimize these effects in YYC and across Canada.</p>
<h2 id="municipal-proposals">Municipal Proposals</h2>
<ol type="1">
<li>
<p>Public Transit</p>

<ol type="1">
<li>
<p>Data</p>

<ol type="1">
<li>
<p>The most effective way to combat urban sprawl is to improve transit accessibility, which in turn reduces vehicular emissions and the need for space-occupying parking lots.<a href="#fn1" class="footnote-ref" id="fnref1" role="doc-noteref"><sup>1</sup></a></p>
</li>
<li>
<p>Transit improves worker commutes, increasing the chances of workers staying employed, reducing societal reliance for unemployment insurance.<a href="#fn2" class="footnote-ref" id="fnref2" role="doc-noteref"><sup>2</sup></a></p>
</li>
<li>
<p>Improved access to public transit fosters inclusivity to the elderly, students, low income/single income families, tourists/visitors, and the differently abled, fostering community integration.<a href="#fn3" class="footnote-ref" id="fnref3" role="doc-noteref"><sup>3</sup></a> </p>
</li>
<li>
<p>Transit is a safer form of travel when compared to private vehicles.<a href="#fn4" class="footnote-ref" id="fnref4" role="doc-noteref"><sup>4</sup></a></p>
</li>
<li>
<p>Luxembourg has become the first country in the world to make all public transit free starting in 2020 to reduce traffic congestion. Currently, fares only generate 10% of the revenue for transit, making the switch relatively inexpensive.<a href="#fn5" class="footnote-ref" id="fnref5" role="doc-noteref"><sup>5</sup></a></p>
</li>
<li>
<p>Transit boarding cards are being used across the world, such as in the Netherlands,<a href="#fn6" class="footnote-ref" id="fnref6" role="doc-noteref"><sup>6</sup></a> London,<a href="#fn7" class="footnote-ref" id="fnref7" role="doc-noteref"><sup>7</sup></a> and New Zealand.<a href="#fn8" class="footnote-ref" id="fnref8" role="doc-noteref"><sup>8</sup></a> These systems are convenient, easy to use, eliminates change, reduces paper waste, and reduces overspending for tickets.</p>
</li>
<li>
<p>24-hour transit availability offered in other cities, such as Berlin,<a href="#fn9" class="footnote-ref" id="fnref9" role="doc-noteref"><sup>9</sup></a> Copenhagen,<a href="#fn10" class="footnote-ref" id="fnref10" role="doc-noteref"><sup>10</sup></a> Chicago,<a href="#fn11" class="footnote-ref" id="fnref11" role="doc-noteref"><sup>11</sup></a> and NYC.<a href="#fn12" class="footnote-ref" id="fnref12" role="doc-noteref"><sup>12</sup></a></p>
</li>
</ol></li>
