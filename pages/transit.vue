</li>
<li>
<p>Suburban neighborhoods were built to accommodate cars, but increasing poverty is forcing many individuals to walk, causing increased pedestrian accidents from inadequate sidewalk systems.<a href="#fn17" class="footnote-ref" id="fnref17" role="doc-noteref"><sup>17</sup></a></p>
</li>
</ol></li>
<li>
<p>Proposals</p>

<ol start="23" type="1">
