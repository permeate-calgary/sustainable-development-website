<h2 id="provincial-proposals">Provincial Proposals</h2>
<ol start="5" type="1">
<li><h3 id="high-speed-commuter-train-service-between-calgary-and-edmonton">High-speed commuter train service between Calgary and Edmonton</h3>
<ol start="9" type="1">
<li>
<p>Data</p>

<ol start="28" type="1">
<li>
<p>HWY 2 currently trafficked by 32,000 vehicles per day on average or more throughout this corridor as of 2016.<a href="#fn20" class="footnote-ref" id="fnref20" role="doc-noteref"><sup>20</sup></a></p>
</li>
<li>
<p>A high-speed train could add anywhere between $5.7 and $41.5 billion to the Albertan economy (adjusted for inflation), create 3,400-7,162 permanent jobs, and decrease vehicular emissions.<a href="#fn21" class="footnote-ref" id="fnref21" role="doc-noteref"><sup>21</sup></a></p>
</li>
<li>
<p>TransPod, a private corporation, will begin construction of a bullet-train in 2021 between Olds and Didsbury and will complete the Calgary-Edmonton line by 2030.<a href="#fn22" class="footnote-ref" id="fnref22" role="doc-noteref"><sup>22</sup></a> The current construction project is valued at $6 billion dollars.<a href="#fn23" class="footnote-ref" id="fnref23" role="doc-noteref"><sup>23</sup></a></p>
</li>
</ol></li>
<li>
<p>Proposals</p>

<ol start="31" type="1">
<li>
<p>The Government of Alberta must invest in developing a high-speed rail system in the Calgary-Edmonton corridor.</p>
</li>
<li>
<p>Run the high-speed train service as a public corporation rather than a private business.</p>
</li>
</ol></li>
</ol></li>
</ol>
<h2 id="federal-proposals">Federal Proposals</h2>
<ol start="6" type="1">
<li>
<p>Trans-Canadian Railway</p>

<ol start="11" type="1">
<li>
<p>Data</p>

<ol start="33" type="1">
<li>
<p>Currently, one-way express passenger services between Vancouver and Toronto is a four-day trip costing upwards of $2699 CAD<a href="#fn24" class="footnote-ref" id="fnref24" role="doc-noteref"><sup>24</sup></a> or a seven-day trip costing upwards of $1660.<a href="#fn25" class="footnote-ref" id="fnref25" role="doc-noteref"><sup>25</sup></a></p>
</li>
<li>
<p>Non-stop one-way plane tickets between Vancouver and Toronto is a four-hour trip costing less than $200.<a href="#fn26" class="footnote-ref" id="fnref26" role="doc-noteref"><sup>26</sup></a></p>
</li>
<li>
<p>Trains produce far less emissions per kilometer compared to planes, buses, and individual automobiles.<a href="#fn27" class="footnote-ref" id="fnref27" role="doc-noteref"><sup>27</sup></a></p>
</li>
<li>
<p>Rail privatization in the UK has led to increased prices, decreased punctuality, greater taxpayer cost, and reduced customer satisfaction. British rail privatization was done to undermine organized labor at the expense of an essential public utility.<a href="#fn28" class="footnote-ref" id="fnref28" role="doc-noteref"><sup>28</sup></a></p>
</li>
</ol></li>
<li>
<p>Proposals</p>

<ol start="37" type="1">
<li>
<p>Nationalize Canadian rail to improve the quality and price of train-travel in Canada.</p>
</li>
<li>
<p>Research the feasibility of implementing high-speed rail between Vancouver and Toronto.</p>
</li>
</ol></li>
