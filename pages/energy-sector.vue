	<h1 id="energy-sector">Energy Sector</h1>
	<h2 id="introduction">Introduction</h2>
	<p>Fossil fuels are the primary cause of climate change. While sustainable energy generation exists, ending fossil fuel industry strangleholds, expanding renewable energy generation, ensuring a Just Transition for workers are the primary hurdles sustainable energy development. These measures require not only infrastructural changes, but corporate restructuring and employment transition for energy sector workers. The following data and proposals of our Maximum GND emphasize how this is not only feasible in, but beneficial for, our current economic and social paradigm.</p>
	<h2 id="municipal-level-proposals">Municipal Level Proposals</h2>
	<h3 id="microgrids">Microgrids</h3>
	<ol type="1">
	<li>
	<p>Microgrid Power Generation</p>

	<ol type="1">
	<li>
	<p>Data</p>

	<ol type="1">
	<li>
	<p>Microgrids are local autonomous energy grids, connected to a main grid, but capable of generating electricity independently.<a href="#fn1" class="footnote-ref" id="fnref1" role="doc-noteref"><sup>1</sup></a>  </p>
	</li>
	<li>
	<p>Microgrids are highly resilient to natural disasters, allowing communities to recover from power outages that may result.<a href="#fn2" class="footnote-ref" id="fnref2" role="doc-noteref"><sup>2</sup></a>  </p>
	</li>
	<li>
	<p>Using a decentralized optimization algorithms, excess electricity can be shared with other members of the microgrid when energy yields are higher.<a href="#fn3" class="footnote-ref" id="fnref3" role="doc-noteref"><sup>3</sup></a>  </p>
	</li>
	<li>
	<p>Reduced spending on energy from microgrids keeps wealth within the community.<a href="#fn4" class="footnote-ref" id="fnref4" role="doc-noteref"><sup>4</sup></a></p>
	</li>
	<li>
	<p>Edmonton has increased rebates for solar power to $0.40/Wt, covering about 15% of the cost of installing a solar system on an existing home.<a href="#fn5" class="footnote-ref" id="fnref5" role="doc-noteref"><sup>5</sup></a></p>
	</li>
	</ol></li>
	<li>
	<p>Proposals</p>

	<ol start="6" type="1">
	<li>
	<p>Create community projects, co-operatives, and trusts to install renewable energy generation and storage capacity.</p>
	</li>
	<li>
	<p>Offer educational opportunities and job retraining to individuals interested in microgrid installation and maintenance.</p>
	</li>
	<li>
	<p>Offer tax deductions for new buildings with microgrid solar generating capacity.</p>
	</li>
	<li>
	<p>Offer rebates and grants for solar retrofits, similar to the Edmonton’s solar energy rebate program.</p>
	</li>
	<li>
	<p>Mandate smart meter installation to allow buildings to send electricity back to the grid.</p>
	</li>
	<li>
	<p>Streamline approval process for businesses installing local generation.</p>
	</li>
	</ol></li>
	</ol></li>
	</ol>
	<h3 id="worker-cooperatives">Worker Cooperatives</h3>
	<ol start="2" type="1">
	<li>
	<p>Worker Co-ops</p>

	<ol start="3" type="1">
	<li>
	<p>Data</p>

	<ol start="12" type="1">
	<li>
	<p>Worker co-ops are more productive than conventional firms and increase employment security for workers.<a href="#fn6" class="footnote-ref" id="fnref6" role="doc-noteref"><sup>6</sup></a></p>
	</li>
	<li>
	<p>Worker co-ops can help localize neighborhoods by pooling resources to purchase or lease property in the city, allowing for mutual benefit for all in the co-op.<a href="#fn7" class="footnote-ref" id="fnref7" role="doc-noteref"><sup>7</sup></a></p>
	</li>
	<li>
	<p>Despite their reputation for small-scale ventures, worker co-ops can be large and successful. Mondragon in Spain is one of the most prominent worker coop examples, valued at €15 billion and employing 84,000 people. Evergreen Cooperative Laundry in Cleveland, Ohio is a recent success story that in the course of ten years became an $8 million USD operation.<a href="#fn8" class="footnote-ref" id="fnref8" role="doc-noteref"><sup>8</sup></a></p>
	</li>
	<li>
	<p>Tax-breaks, funding, technical assistance, and legislation has helped establish worker’s co-ops as economic mainstays in Italy, France, and Spain.<a href="#fn9" class="footnote-ref" id="fnref9" role="doc-noteref"><sup>9</sup></a></p>
	</li>
	<li>
	<p>Solar energy generation strongly benefits worker co-ops. Solar co-ops generate profit and help communities save on energy costs by up to 20%.<a href="#fn10" class="footnote-ref" id="fnref10" role="doc-noteref"><sup>10</sup></a></p>
	</li>
	<li>
	<p>Proposals</p>

	<ol type="1">
	<li>
	<p>Offer tax breaks and funds to encourage the growth of worker’s co-ops.</p>
	</li>
	<li>
	<p>Offer technical assistance to start-up worker’s co-ops, including offering publicly-available information for workers interested in starting a co-op.</p>
	</li>
	<li>
	<p>Grant workers the right of first refusal so they can offer to buy-out companies that are closing, planning on going public, or planning on selling to larger companies. This will include low-interest loans and grants to facilitate this transition.</p>
	</li>
	<li>
	<p>Provide information to worker co-ops to establish renewable energy generation</p>
	</li>
	</ol></li>
	</ol></li>
	</ol></li>
	</ol>
	<h2 id="provincial-level-proposals">Provincial Level Proposals</h2>
	<h3 id="provincial-renewable-energy">Provincial Renewable Energy</h3>
	<ol start="3" type="1">
	<li>
	<p>Provincial Renewables</p>

	<ol start="4" type="1">
	<li>
	<p>Data</p>

	<ol start="18" type="1">
	<li>
	<p>Alberta emits 262.3 million tons of CO2 (as of 2016) annually, the most of any Canadian province. 48% of these emissions stem from the oil and gas industry, 17% stem from electricity generation, and 12% stem from transportation.<a href="#fn11" class="footnote-ref" id="fnref11" role="doc-noteref"><sup>11</sup></a></p>
	</li>
	<li>
	<p>Alberta consumes most of its energy in the form of natural gas - the majority of which is used to provide heat and electricity for extracting petroleum from the oil sands.<a href="#fn12" class="footnote-ref" id="fnref12" role="doc-noteref"><sup>12</sup></a></p>
	</li>
	<li>
	<p>Because of its reliance on coal for electricity generation, in 2016, Alberta’s electricity sector accounts for 58% of CO2 emissions for Canada’s electricity sector.<a href="#fn13" class="footnote-ref" id="fnref13" role="doc-noteref"><sup>13</sup></a>  </p>
	</li>
	<li>
	<p>Alberta’s former renewable energy program resulted in the installation of 1,363 megawatts of wind energy, at an average cost of 3.84 cents per kilowatt-hour (c/kWh) <a href="#fn14" class="footnote-ref" id="fnref14" role="doc-noteref"><sup>14</sup></a>. For reference, the average price of electricity in Alberta, from October 2 to 8, was 3.15 c/kWh.<a href="#fn15" class="footnote-ref" id="fnref15" role="doc-noteref"><sup>15</sup></a></p>
	</li>
	</ol></li>
	<li>
	<p>Proposals</p>

	<ol start="22" type="1">
	<li>
	<p>Reinstate Alberta’s renewable energy procurement program.</p>
	</li>
	<li>
	<p>Implement more aggressive renewable portfolio standards.</p>
	</li>
	<li>
	<p>Reinstate provincial carbon tax at 20$ per equivalent ton of CO2.</p>
	</li>
	<li>
	<p>Freeze and progressively cap oilsands and natural gas extraction.</p>
	</li>
	<li>
	<p>Raise consumer E.V. incentives to meet the standards set by BC and Quebec.</p>
	</li>
	<li>
	<p>Mandate indigenous (per UN Declaration on the Rights of Indigenous Peoples) and local consultation on energy projects. Expand the qualifications specified by the REP to all energy infrastructure projects, and require that companies invest in local communities by hiring locally, creating local projects, or by direct and fair compensation.</p>
	</li>
	</ol></li>
	</ol></li>
	</ol>
	<h3 id="provincialize-non-renewable-energy">Provincialize Non-Renewable Energy</h3>
	<ol start="4" type="1">
	<li>
	<p>Provincialize Non-Renewable Energy</p>

	<ol start="6" type="1">
	<li>
	<p>Data</p>

	<ol start="28" type="1">
	<li>
	<p>Declaring an intention to nationalize fossil fuel industries would decrease the cost of the shares in fossil fuel companies and disincentivize private investment.<a href="#fn16" class="footnote-ref" id="fnref16" role="doc-noteref"><sup>16</sup></a> Precedent for such a drop in share value was when the Obama administration announced it would phase out private prisons.<a href="#fn17" class="footnote-ref" id="fnref17" role="doc-noteref"><sup>17</sup></a></p>
	</li>
	<li>
	<p>SaskOil is a precedent for a provincially owned oil &amp; gas corporation, established specifically to prevent fossil fuel companies from controlling provincial politics.<a href="#fn18" class="footnote-ref" id="fnref18" role="doc-noteref"><sup>18</sup></a>  </p>
	</li>
	</ol></li>
	<li>
	<p>Proposals</p>

	<ol start="30" type="1">
	<li>
	<p>Declare an intention to provincialize fossil fuel companies to decrease the price of shares.</p>
	</li>
	<li>
	<p>Compulsorily buy 51% of shares and elect majority board dedicated to phase out fossil fuels.</p>
	</li>
	<li>
	<p>Profits from the corporations will be used to hold over costs, keep jobs until Just Transition is implemented.</p>
	</li>
	</ol></li>
	</ol></li>
	</ol>
	<h2 id="federal-level-proposals">Federal Level Proposals</h2>
	<h3 id="de-prioritizing-non-renewable-energy">De-Prioritizing Non-Renewable Energy</h3>
	<ol start="5" type="1">
	<li>
	<p>De-Prioritizing Non-Renewable Energy</p>

	<ol start="8" type="1">
	<li>
	<p>Data</p>

	<ol start="33" type="1">
	<li>
	<p>Global fossil fuel subsidies cost $373 billion USD per year. Canada itself pays $3.3 billion in subsidies to the fossil fuel industry, or $19/tonne of carbon pollution.<a href="#fn19" class="footnote-ref" id="fnref19" role="doc-noteref"><sup>19</sup></a>   <a href="#fn20" class="footnote-ref" id="fnref20" role="doc-noteref"><sup>20</sup></a></p>
	</li>
	<li>
	<p>Cutting fossil fuel subsidies and carbon pricing do not slow economic growth in over 70 different national and subnational economies.<a href="#fn21" class="footnote-ref" id="fnref21" role="doc-noteref"><sup>21</sup></a>  </p>
	</li>
	<li>
	<p>Investment in energy efficiency saves billions of dollars in energy waste and creates three times as many jobs as investing the same amount into the fossil fuel industry.<a href="#fn22" class="footnote-ref" id="fnref22" role="doc-noteref"><sup>22</sup></a></p>
	</li>
	<li>
	<p>Investing in green energy training (such as solar technicians) for women has accelerated market growth and promoted a green energy transition.<a href="#fn23" class="footnote-ref" id="fnref23" role="doc-noteref"><sup>23</sup></a></p>
	</li>
	</ol></li>
	<li>
	<p>Proposals</p>

	<ol start="37" type="1">
	<li>
	<p>End subsidies to non-renewable energy.</p>
	</li>
	<li>
	<p>Introduce carbon pricing to non-renewable energy.</p>
	</li>
	<li>
	<p>Invest capital in energy efficiency conversions.</p>
	</li>
	<li>
	<p>Invest in training women and indigenous people in green energy jobs.</p>
	</li>
	</ol></li>
	</ol></li>
	</ol>
	<h3 id="green-innovation-fund">Green Innovation Fund</h3>
	<ol start="6" type="1">
	<li>
	<p>Green Innovation Fund</p>

	<ol start="10" type="1">
	<li>
	<p>Data</p>

	<ol start="41" type="1">
	<li>
	<p>Development of new renewable energy technologies is facing market-failure, where the risk of developing new technology is too high to be profitable, requiring government intervention.<a href="#fn24" class="footnote-ref" id="fnref24" role="doc-noteref"><sup>24</sup></a></p>
	</li>
	<li>
	<p>The Clean Growth Hub is a Canadian agency that funds green energy R&amp;D, with $2.3 billion in funds during 2017.<a href="#fn25" class="footnote-ref" id="fnref25" role="doc-noteref"><sup>25</sup></a></p>
	</li>
	<li>
	<p>Green New Deal Institutes could be used to subsidize the cost of developing mid-stage technology research tech.<a href="#fn26" class="footnote-ref" id="fnref26" role="doc-noteref"><sup>26</sup></a></p>
	</li>
	<li>
	<p>Creating trusts in developing technology sectors has been done in the past to encourage economic participation, paying dividends to investors should the technology be successful and marketable.<a href="#fn27" class="footnote-ref" id="fnref27" role="doc-noteref"><sup>27</sup></a></p>
	</li>
	<li>
	<p>Many tech companies notoriously avoid paying taxes, so making tech firms that use green energy technology pay royalties would be a way to co-opt funds that would otherwise be lost.<a href="#fn28" class="footnote-ref" id="fnref28" role="doc-noteref"><sup>28</sup></a></p>
	</li>
	</ol></li>
	<li>
	<p>Proposals</p>

	<ol start="46" type="1">
	<li>
	<p>Expand the Clean Growth Hub by increasing funding to its projects and challenging the agency to help Canada develop green innovation through experimental green technology.</p>
	</li>
	<li>
	<p>Establish Green New Deal Institutes to subsidize the cost of R&amp;D for mid-stage technology.</p>
	</li>
	<li>
	<p>Encourage investment of green innovation by setting up a public trust in green technology R&amp;D.</p>
	</li>
	<li>
	<p>Encourage tech companies to invest in green technology by offering their proprietary use with small royalty payments rather than through low-interest loans, helping to recycle funds into more green innovation projects.</p>
	</li>
	</ol></li>
	</ol></li>
	</ol>
	<h3 id="just-transition">Just Transition</h3>
	<ol start="7" type="1">
	<li>
	<p>Workplace Transition Plans</p>

	<ol start="12" type="1">
	<li>
	<p>Data</p>

	<ol start="50" type="1">
	<li>
	<p>If allowed to “bust” by a sudden loss of investor confidence, fossil fuel workers could suddenly lose their jobs without skills or resources to access new employment.<a href="#fn29" class="footnote-ref" id="fnref29" role="doc-noteref"><sup>29</sup></a>  </p>
	</li>
	<li>
	<p>Clear timelines will help mitigate economic turmoil from transitioning to renewable energy.<a href="#fn30" class="footnote-ref" id="fnref30" role="doc-noteref"><sup>30</sup></a></p>
	</li>
	</ol></li>
	<li>
	<p>Proposals</p>

	<ol start="52" type="1">
	<li>
	<p>Create clear timelines for reduction and closure of fossil fuel plants and projects.</p>
	</li>
	<li>
	<p>Create strategies for employee attrition well in advance of closures.</p>
	</li>
	<li>
	<p>Develop strategies for community resilience if extractive fuel industries no longer operate in a community, particularly through renewable energy systems.</p>
	</li>
	</ol></li>
	</ol></li>
	<li>
	<p>Retraining and Job Transfers</p>

	<ol start="14" type="1">
	<li>
	<p>Data</p>

	<ol start="55" type="1">
	<li>
	<p>Decarbonization will create millions of new jobs by 2050.<a href="#fn31" class="footnote-ref" id="fnref31" role="doc-noteref"><sup>31</sup></a>  </p>
	</li>
	<li>
	<p>The fossil fuel industry today accounts for only 1% of jobs and 8% of the national GDP.<a href="#fn32" class="footnote-ref" id="fnref32" role="doc-noteref"><sup>32</sup></a></p>
	</li>
	<li>
	<p>Employment Insurance would not be sufficient for former fossil fuel workers exiting the industry.<a href="#fn33" class="footnote-ref" id="fnref33" role="doc-noteref"><sup>33</sup></a></p>
	</li>
	<li>
	<p>Alberta is the only province that has taken steps to create a Just Transition for coal workers, with $195 million allocated to help communities transfer to renewables and later an additional $30 million to support fossil fuel workers.<a href="#fn34" class="footnote-ref" id="fnref34" role="doc-noteref"><sup>34</sup></a></p>
	</li>
	<li>
	<p>Quebec has a payroll tax levy on companies that do not train apprenticeships to at least 1% of their workforce.<a href="#fn35" class="footnote-ref" id="fnref35" role="doc-noteref"><sup>35</sup></a>  </p>
	</li>
	<li>
	<p>China has delayed or stopped development of 151 coal power plants, instead investing $15 billion USD into job retraining, relocating, and retirement to coal workers.<a href="#fn36" class="footnote-ref" id="fnref36" role="doc-noteref"><sup>36</sup></a></p>
	</li>
	</ol></li>
	<li>
	<p>Proposals</p>

	<ol start="61" type="1">
	<li>
	<p>Create a coordinated plan to transfer and retrain individuals in all fossil fuel industries.</p>
	</li>
	<li>
	<p>Allocate funding to retrain and transfer fossil fuel workers to renewable energy industries or to other areas where their skills are transferable, such as truck drivers transferring to urban transit or electrical technicians transferring to renewable energy.</p>
	</li>
	<li>
	<p>Allocate funding to communities that rely on fossil fuel employment to establish alternative industries, such as renewable energy generation or agriculture.</p>
	</li>
	<li>
	<p>Invest in expanding apprenticeship programs in energy related fields.</p>
	</li>
	<li>
	<p>Encourage indigenous retraining and job transfers by setting aside a retraining fund specifically for indigenous workers.</p>
	</li>
	</ol></li>
	</ol></li>
	<li>
	<p>Pensions</p>

	<ol start="16" type="1">
	<li>
	<p>Data</p>

	<ol start="66" type="1">
	<li>
	<p>Older workers are often passed over for up-training and retraining, due to their shorter span in the industry.<a href="#fn37" class="footnote-ref" id="fnref37" role="doc-noteref"><sup>37</sup></a></p>
	</li>
	</ol></li>
	<li>
	<p>Proposals</p>

	<ol start="67" type="1">
	<li>
	<p>Governments should provide funding to “bridge” older workers to retirement. Bridging is easier and more cost-effective than training workers for jobs they will only briefly perform before retiring. This can be done through already-existing infrastructure found in EI.</p>
	</li>
	<li>
	<p>Develop pension criteria in partnership with employers, unions and government stakeholders.</p>
	</li>
	<li>
	<p>Audit existing pension to ensure adequate funding exists for both income and benefits if the underlying firm or industry disappears.</p>
	</li>
	<li>
	<p>Offer early retirement for workers nearing retirement age.</p>
	</li>
	</ol></li>
	</ol></li>
	<li>
	<p>Community Transition</p>

	<ol start="18" type="1">
	<li>
	<p>Data</p>

	<ol start="71" type="1">
	<li>
	<p>Most transition plans neglect to account for cases where a worker loses their job in a household, but another household member does not.<a href="#fn38" class="footnote-ref" id="fnref38" role="doc-noteref"><sup>38</sup></a></p>
	</li>
	</ol></li>
	<li>
	<p>Proposals</p>

	<ol start="72" type="1">
	<li>
	<p>Offer employment and mental health counselling services to workers who are transitioning out of the fossil fuel industry.</p>
	</li>
	<li>
	<p>Help families with dual-incomes find employment through in new communities via family employment counselling.</p>
	</li>
	</ol></li>
	</ol></li>
	</ol>
