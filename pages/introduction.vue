	<h2 id="the-solutions">The Solutions</h2>
	<ul>
	<li><p>A <a href="https://www.cnbc.com/2019/02/07/aoc-just-updated-her-massive-green-new-deal--heres-whats-in-it.html">Green New Deal</a> (GND) is the solution to the social and ecological problems we currently face.</p></li>
	<li><p>A GND would be a public spending initiative to rapidly transform our energy systems and develop climate-resistant infrastructure, all while employing <a href="http://www.civicgovernance.ca/wordpress/wp-content/uploads/2017/08/Columbia-Jobs-for-Tomorrow-web-low-res-June-30-FINAL.pdf">millions</a> of people and <a href="https://canadians.org/sites/default/files/publications/Green%20Decent%20and%20Public%20-%20Report%20Final.pdf">reviving</a> our economy.</p></li>
	<li><p>A GND emphasizes timely, achievable, inclusive actions developed from successful examples from across the world. We aim to avoid insubstantial targets that popular political parties have developed to quell meaningful change. Maintaining the status quo is incompatible with impactful climate action.</p></li>
	<li><p>Integral to a GND is the respect of the sovereignty of <a href="http://www.ienearth.org/talking-points-on-the-aoc-markey-green-new-deal-gnd-resolution/">First Nations</a>, decolonization, and the effective implementation of the United Nations Declaration of the Rights of Indigenous Peoples, which recognizes that the protection of indigenous rights protects the environment.</p></li>
