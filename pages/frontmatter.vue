<h2 id="a-maximum-green-new-deal-is-the-solution">A Maximum Green New Deal is the Solution</h2>
<ul>
<li><p>A <a href="https://www.cnbc.com/2019/02/07/aoc-just-updated-her-massive-green-new-deal--heres-whats-in-it.html"><span class="underline">Green New Deal</a> (GND) would be a large public spending blitz to rapidly transform our energy systems and develop climate-resistant infrastructure, all while employing <a href="http://www.civicgovernance.ca/wordpress/wp-content/uploads/2017/08/Columbia-Jobs-for-Tomorrow-web-low-res-June-30-FINAL.pdf"><span class="underline">millions</a> of people and <a href="https://canadians.org/sites/default/files/publications/Green%20Decent%20and%20Public%20-%20Report%20Final.pdf"><span class="underline">reviving</a> our economy.</p></li>
<li><p>A Maximum GND emphasizes timely, achievable, inclusive actions developed from successful examples from across the world. We aim to avoid insubstantial targets that popular political parties have developed to quell meaningful change. Maintaining the status quo is incompatible with impactful climate action.</p></li>
<li><p>Integral to a Maximum GND is the respect of the sovereignty of <a href="http://www.ienearth.org/talking-points-on-the-aoc-markey-green-new-deal-gnd-resolution/"><span class="underline">First Nations</a>, decolonization, and the actual implementation of the United Nations Declaration of the Rights of Indigenous Peoples, which recognizes that the protection of indigenous rights protects the environment.</p></li>
<li><p>Ecological conservation and recovery work, and the role that <a href="https://www.youtube.com/watch?v=s_kLye_6VBc"><span class="underline">worker cooperatives</a> and <a href="http://canadianlabour.ca/sites/default/files/media/GEN-Common-Platform-2016-EN.pdf"><span class="underline">union</a>s can play in accomplishing this transformative change, create the foundational for a sustainable culture and economy.</p></li>
