<h2 id="on-worker-cooperatives">On Worker Cooperatives</h2>
<p>Worker cooperatives are employee-owned businesses in which decisions are made democratically: one worker, one vote. Worker coops are an innovative model of keeping wealth local and incomes high, while ensuring the energy grid of the future is accountable to the community.</p>
<ul>
<li>
<p>Efficiency, feasibility, profitability, potential</p>

<ul>
<li>
<p>Worker coops are <a href="https://www.thenation.com/article/worker-cooperatives-are-more-productive-than-normal-companies/"><span class="underline">more productive</a> than conventional firms.</p>
</li>
<li>
<p>A more in-depth <a href="https://www.uk.coop/sites/default/files/uploads/attachments/worker_co-op_report.pdf"><span class="underline">study</a> on worker coops that discusses their size, profitability, viability in different sectors, etc.</p>
</li>
<li>
<p><a href="https://www.theguardian.com/world/2013/mar/07/mondragon-spains-giant-cooperative"><span class="underline">Mondragon</a> is one of the most prominent worker coop examples: they compete globally, and have a university which welcomes students from GM and Microsoft.</p>
</li>
</ul></li>
<li>
<p>The <a href="https://www.youtube.com/watch?v=s_kLye_6VBc"><span class="underline">Evergreen Model</a> is a prime example of how worker cooperatives can</p>

<ul>
<li>
<p>revive impoverished areas in an environmentally and economically sustainable way</p>
</li>
<li>
<p>naturally build wealth more equitably,</p>
</li>
<li>
<p>and provide a reliable tax base for local government.</p>
</li>
</ul></li>
<li>
<p>Policies</p>

<ul>
<li>
<p>Increased <a href="http://canadianworker.coop/wp-content/uploads/2012/06/CWCF_Research_Paper_International_16-6-2010_fnl1.pdf"><span class="underline">supportive policies</a> must be made to encourage the growth of and educate people about worker cooperatives in order to build the sector.</p>
</li>
<li>
<p>Allow workers “the right of first refusal”: the right to refuse corporate buyout. <a href="https://www.jacobinmag.com/2019/04/worker-ownership-private-equity-cooperatives"><span class="underline">article</a>.</p>
</li>
<li>
<p>More information may be requested by Students for Direct Action or the Canadian Worker Cooperative Federation on this topic.</p>
