<p>Rationale: Local organic food production</p>

<ol type="1">
<li>
<p>Climate change will cause changes in rainfall, sea-level increases, and extreme weather events, threatening local, national, and international food security.<a href="#fn1" class="footnote-ref" id="fnref1" role="doc-noteref"><sup>1</sup></a> Extreme weather events have nearly doubled since the 1990s, threatening food security on a global scale.<a href="#fn2" class="footnote-ref" id="fnref2" role="doc-noteref"><sup>2</sup></a> Developing local food sources in Calgary will help mitigate rising food insecurity in the city and region.</p>
</li>
<li>
<p>Local food production provides mitigates carbon production, democratizes food access, and establishes community-level organization absent in contemporary Canadian cities.<a href="#fn3" class="footnote-ref" id="fnref3" role="doc-noteref"><sup>3</sup></a></p>
</li>
<li>
<p>Gardening reduces anxiety, depression, and obesity while increasing perceived quality of life and sense of community.<a href="#fn4" class="footnote-ref" id="fnref4" role="doc-noteref"><sup>4</sup></a></p>
</li>
</ol></li>
