</li>
<li>
<p>The rising cost of food worries nine in ten Canadians, with food prices expected to rise by $400 per year for Canadian families.<a href="#fn8" class="footnote-ref" id="fnref8" role="doc-noteref"><sup>8</sup></a></p>
</li>
</ol></li>
