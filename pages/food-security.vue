<h3 id="plant-based-diets">Plant-Based Diets</h3>
<ol start="9" type="1">
<li>
<p>Data</p>

<ol start="21" type="1">
<li>
<p>Animal agriculture is one of the leading causes of climate change, accounting for 80% of human land use and 18% of all carbon emissions.<a href="#fn50" class="footnote-ref" id="fnref50" role="doc-noteref"><sup>50</sup></a> Livestock account for 30% of all anthropogenic methane emissions.<a href="#fn51" class="footnote-ref" id="fnref51" role="doc-noteref"><sup>51</sup></a></p>
</li>
<li>
<p>Global transition to low-meat or meatless diets could result in 2.8 Gha of reclaimed land that could be used as cropland or as a natural vegetation carbon-sink.<a href="#fn52" class="footnote-ref" id="fnref52" role="doc-noteref"><sup>52</sup></a> The cost of maintaining CO2 below 450 ppm by 2050 would be 55% less if we shifted toward a plant-based diet.<a href="#fn53" class="footnote-ref" id="fnref53" role="doc-noteref"><sup>53</sup></a>  The individual difference in shifting toward a plant-based diet is similar to the difference of a sedan versus an SUV.<a href="#fn54" class="footnote-ref" id="fnref54" role="doc-noteref"><sup>54</sup></a>  Meat-consumption in the United States is equal to the carbon dioxide produced by 60 million vehicles.<a href="#fn55" class="footnote-ref" id="fnref55" role="doc-noteref"><sup>55</sup></a></p>
</li>
<li>
<p>Livestock populations suffer enormous losses during droughts and famines. For example, during droughts in Africa from 1981 to 1999, between 20% and 62% of cattle died.<a href="#fn56" class="footnote-ref" id="fnref56" role="doc-noteref"><sup>56</sup></a> Climate change will exacerbate these trends in the future, making livestock agriculture potentially untenable on a large-scale.<a href="#fn57" class="footnote-ref" id="fnref57" role="doc-noteref"><sup>57</sup></a> The FAO recommends that we should “shift away” from grain-fed livestock species as a matter of global food security.<a href="#fn58" class="footnote-ref" id="fnref58" role="doc-noteref"><sup>58</sup></a></p>
</li>
<li>
<p>Livestock grazing is one of the leading causes of global deforestation, destroying important habitats and removing carbon sinks.<a href="#fn59" class="footnote-ref" id="fnref59" role="doc-noteref"><sup>59</sup></a></p>
</li>
<li>
<p>Plant-based diets confer numerous prevent obesity, cardiovascular disease, and diabetes.<a href="#fn60" class="footnote-ref" id="fnref60" role="doc-noteref"><sup>60</sup></a> The World Health Organization classifies processed meats as a group 1 carcinogen (“carcinogenic to humans”) and red meat as a group 2A carcinogen (“probably carcinogenic to humans”).<a href="#fn61" class="footnote-ref" id="fnref61" role="doc-noteref"><sup>61</sup></a>  Thus, widespread adoption of plant-based food would decrease the burden on the healthcare system.</p>
</li>
<li>
<p>Nearly 43% of Canadians are currently shifting to a partly or completely plant-based diet, largely for health and wellness reasons.<a href="#fn62" class="footnote-ref" id="fnref62" role="doc-noteref"><sup>62</sup></a></p>
</li>
<li>
<p>Canadian agricultural subsidies were worth $6.9 billion in 2011 with the majority of the money going toward beef, egg, and pork production.<a href="#fn63" class="footnote-ref" id="fnref63" role="doc-noteref"><sup>63</sup></a>  In Alberta alone, the Canadian Agricultural Partnership invested $406 million in the agricultural sector, largely for meat and egg production.<a href="#fn64" class="footnote-ref" id="fnref64" role="doc-noteref"><sup>64</sup></a></p>
</li>
<li>
<p>Canadians consumed 94 million Kg of beef less beef in 2019 than in 2010 and prices are on a steady decline while plant-based sources are increasing in price.<a href="#fn65" class="footnote-ref" id="fnref65" role="doc-noteref"><sup>65</sup></a> Unfortunately, producers are not adapting to this change (or acknowledging the change), threatening their livelihoods being crowded out by plant-based alternatives.<a href="#fn66" class="footnote-ref" id="fnref66" role="doc-noteref"><sup>66</sup></a></p>
</li>
<li>
<p>Slaughterhouses are one of the most dangerous workplaces, with a greater than 100% turnover rate annually (sometimes up to 250%), a 25% rate of serious injury requiring medical attention, and a greater than 60% prevalence of carpal tunnel syndrome.<a href="#fn67" class="footnote-ref" id="fnref67" role="doc-noteref"><sup>67</sup></a> Most slaughterhouse workers are people of color of largely immigrant and uneducated backgrounds, paid minimum wage.<a href="#fn68" class="footnote-ref" id="fnref68" role="doc-noteref"><sup>68</sup></a></p>
</li>
</ol></li>
<li>
<p>Proposals</p>

<ol start="30" type="1">
<li>
<p>End federal and provincial subsidies to animal agriculture.</p>
</li>
<li>
<p>Tax processed meat to reduce meat consumption and reobtain healthcare funds lost from treating diseases associated with eating processed meat products.<a href="#fn69" class="footnote-ref" id="fnref69" role="doc-noteref"><sup>69</sup></a></p>
</li>
<li>
<p>Include plant-based alternatives in school lunches and hospital meals.</p>
</li>
<li>
<p>Education about plant-based alternatives. A good example of this was the inclusion of a general “protein” category in Canada’s food guide.<a href="#fn70" class="footnote-ref" id="fnref70" role="doc-noteref"><sup>70</sup></a></p>
</li>
<li>
<p>Support a Just Transition for beef, dairy, poultry, and pork producers in Alberta. Emphasize using their transferable skills such as producing silage/hay for growing food crops or creating pensions for producers nearing retirement.</p>
</li>
<li>
<p>Support a Just Transition for slaughterhouse workers. Create job retraining opportunities and pension programs for injured workers. Emphasize using transferable industrial skills for building green technology and plant-based agriculture.</p>
</li>
</ol></li>
