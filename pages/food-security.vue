<h2 id="municipal-level-proposals">Municipal Level Proposals</h2>
<h3 id="greenbelt-legislation">Greenbelt Legislation</h3>
<ol start="11" type="1">
<li>
<p>Data</p>

<ol type="1">
<li>
<p>A greenbelt is permanently protected land surrounding urban areas to limit urban growth, preserve and revitalize plant and wildlife, and repurpose land for agriculture and food growth.</p>
</li>
<li>
<p>In 2005, the Government of Ontario established a greenbelt surrounding the Golden Horseshoe, a densely populated and quickly growing urban region in southern Ontario which includes the Greater Toronto Area.<a href="#fn71" class="footnote-ref" id="fnref71" role="doc-noteref"><sup>71</sup></a></p>
</li>
<li>
<p>The total area of Ontario’s greenbelt is 7,284km², making it one of the largest greenbelts in the world.<a href="#fn72" class="footnote-ref" id="fnref72" role="doc-noteref"><sup>72</sup></a></p>
</li>
<li>
<p>Ontario’s greenbelt provides over $9.1 billion in economic benefits and 161,000 full-time equivalent jobs.<a href="#fn73" class="footnote-ref" id="fnref73" role="doc-noteref"><sup>73</sup></a></p>
</li>
<li>
<p>Ontario’s greenbelt includes the Niagara Peninsula, a region that hosts about 2.1 million tender fruit trees (peaches, pears, plums, cherries, grapes). These trees produce over 800,000 baskets of fruit per year.<a href="#fn74" class="footnote-ref" id="fnref74" role="doc-noteref"><sup>74</sup></a></p>
</li>
</ol></li>
</ol>
<p></p>
<ol start="6" type="1">
<li>
<p>Another highly fertile special crop region within Ontario’s greenbelt is The Holland Marsh (“Ontario’s vegetable basket”).<a href="#fn75" class="footnote-ref" id="fnref75" role="doc-noteref"><sup>75</sup></a> The Holland Marsh produces lettuce, celery, potatoes, cauliflower, beets, parsnips, and other world crops. The value of these crops is in excess of $50 million per year.<a href="#fn76" class="footnote-ref" id="fnref76" role="doc-noteref"><sup>76</sup></a></p>
</li>
<li>
<p>In addition to being highly productive agricultural regions, greenbelts also preserve biodiversity and can sequester large quantities of carbon.<a href="#fn77" class="footnote-ref" id="fnref77" role="doc-noteref"><sup>77</sup></a></p>
</li>
</ol>
<ol start="12" type="1">
<li>
<p>Proposals</p>

<ol start="8" type="1">
<li>
<p>Establish “urban growth boundaries” around major cities in Alberta. Limiting urban sprawl makes cities easier to access by transit and reduces the use of private single-occupant vehicles.</p>
</li>
<li>
<p>With urban growth boundaries in place, establish greenbelts surrounding major cities in Alberta. Where possible, use greenbelt areas for agriculture, rewilding, preservation and cultivation of biodiversity, and carbon sequestration.</p>
</li>
</ol></li>
</ol>
<h2 id="canadian-food-security">Canadian Food Security</h2>
<ol start="13" type="1">
<li>
<p>Data</p>

<ol type="1">
<li>
<p>Over 30% of all food consumed in Canada is imported, particularly fruits and vegetables, causing the production of 3.3 million tonnes of CO2.<a href="#fn78" class="footnote-ref" id="fnref78" role="doc-noteref"><sup>78</sup></a>  Not only is this pattern of consumption ecologically unsustainable, but economically unsustainable when faced with increasing global food insecurity.</p>
</li>
<li>
<p>Climate change will cause extreme weather events that may jeopardize global food security, endangering Canadian food security.<a href="#fn79" class="footnote-ref" id="fnref79" role="doc-noteref"><sup>79</sup></a></p>
</li>
<li>
<p>Canada’s North is highly food insecure. Nearly 70% of households in Nunavut are food insecure.<a href="#fn80" class="footnote-ref" id="fnref80" role="doc-noteref"><sup>80</sup></a>  Most households in the Territories rely on a mix of wild-caught game/fish and store-bought goods.<a href="#fn81" class="footnote-ref" id="fnref81" role="doc-noteref"><sup>81</sup></a>  Game is sporadic (particularly with a changing climate) and shipped food is prohibitively expensive.<a href="#fn82" class="footnote-ref" id="fnref82" role="doc-noteref"><sup>82</sup></a></p>
</li>
<li>
<p>Canada has lost 70% of its farmers aged 35-50 in the last twenty-five years.<a href="#fn83" class="footnote-ref" id="fnref83" role="doc-noteref"><sup>83</sup></a></p>
</li>
<li>
<p>The largest barrier to young farmers is the cost of land, making farming a profession that is improbable for many would-be farmers.<a href="#fn84" class="footnote-ref" id="fnref84" role="doc-noteref"><sup>84</sup></a></p>
</li>
<li>
<p>Female farmers are more likely to rent land and use outside work to pay for land compared to male farmers, increasing the overhead of women farmers.<a href="#fn85" class="footnote-ref" id="fnref85" role="doc-noteref"><sup>85</sup></a></p>
</li>
<li>
<p>Animal agriculture accounts for 80% of human land use and 18% of all carbon emissions.<a href="#fn86" class="footnote-ref" id="fnref86" role="doc-noteref"><sup>86</sup></a>  If we do not cease the production of grazing animals, it is unlikely that we can avert climate change.<a href="#fn87" class="footnote-ref" id="fnref87" role="doc-noteref"><sup>87</sup></a></p>
</li>
<li>
<p>For land use proposals, see also Monbiot et al., <em>Land for the Many</em>.<a href="#fn88" class="footnote-ref" id="fnref88" role="doc-noteref"><sup>88</sup></a></p>
</li>
</ol></li>
<li>
<p>Proposals</p>

<ol start="9" type="1">
<li>
<p>Create community land trusts in agriculturally productive regions that are commonly owned by the community, allowing for young farmers to own common stakes of land at a reasonable cost.</p>
</li>
<li>
<p>Prevent the monopolization of agriculturally productive land caused by acreages and land-speculation to be bought, divided, and resold to young farmers at a fair price.</p>
</li>
<li>
<p>Create planning committees for forestry and agricultural activities in rural areas, such as is already done for housing planning committees in urban and suburban areas.</p>
</li>
<li>
<p>Prevent the use of farmland for pasture grazing by buying, dividing, and reselling land to young plant farmers.</p>
</li>
<li>
<p>Offer a buy-back program of artificially cleared pasture or grazing land to be reforested as a carbon sink.</p>
</li>
<li>
<p>Create an open-source weather monitoring platform for individuals and organizations to contend with extreme weather events that may occur as a result of climate change, using Environment Canada, university, and citizen scientist weather stations.<a href="#fn89" class="footnote-ref" id="fnref89" role="doc-noteref"><sup>89</sup></a></p>
</li>
<li>
<p>Research and develop crop strains that can resist extreme weather events, are water-efficient, and help maintain a sustainable nutrient balance.<a href="#fn90" class="footnote-ref" id="fnref90" role="doc-noteref"><sup>90</sup></a></p>
</li>
<li>
<p>Protect the right to use uncultivated land for agricultural production.</p>
</li>
<li>
<p>Increase funding to research wildlife game populations in Canada’s North to assess the sustainability of these resources.</p>
</li>
<li>
<p>Regularly communicate research findings to Northern communities.</p>
</li>
<li>
<p>Create and implement game sustainability programs advanced by indigenous leadership.</p>
</li>
</ol></li>
</ol>
